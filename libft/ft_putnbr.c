#include "libft.h"

void	ft_putnbr(int nb)
{
	long long n;

	n = (long long)nb;
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
	}
	if (0 <= n && n < 10)
	{
		ft_putchar(n + '0');
	}
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
